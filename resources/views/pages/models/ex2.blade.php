                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('is-revealed');
                        observer.unobserve(entry.target);
                    }
                });
            }, observerOptions);
            document.querySelectorAll('.reveal-up, .reveal-left, .reveal-right, .reveal-scale').forEach(el => revealObserver.observe(el));

            const btnExt = document.getElementById('btn-tab-exterior');
            const btnInt = document.getElementById('btn-tab-interior');
            const wrapExt = document.getElementById('wrapper-exterior');
            const wrapInt = document.getElementById('wrapper-interior');
            const swatchesExtGroup = document.getElementById('swatches-exterior');
            const swatchesIntGroup = document.getElementById('swatches-interior');
            const colorNameDisplay = document.getElementById('active-color-name');

            // Logic Fitur Drag to Scroll untuk slider horizontal
            document.querySelectorAll('.overflow-x-auto, [data-slider]').forEach(slider => {
                let isDown = false;
                let startX;
                let scrollLeft;
                let isDragging = false;

                slider.addEventListener('mousedown', (e) => {
                    isDown = true;
                    isDragging = false;
                    slider.style.cursor = 'grabbing';
                    startX = e.pageX - slider.offsetLeft;
                    scrollLeft = slider.scrollLeft;
                });
                
                slider.addEventListener('mouseleave', () => {
                    isDown = false;
                    slider.style.cursor = 'grab';
                });
                
                slider.addEventListener('mouseup', () => {
                    isDown = false;
                    slider.style.cursor = 'grab';
                });
                
                slider.addEventListener('mousemove', (e) => {
                    if (!isDown) return;
                    e.preventDefault();
                    isDragging = true;
                    const x = e.pageX - slider.offsetLeft;
                    const walk = (x - startX) * 2;
                    slider.scrollLeft = scrollLeft - walk;
                });

                const clickableItems = slider.querySelectorAll('[onclick], a');
                clickableItems.forEach(item => {
                    item.addEventListener('click', (e) => {
                        if (isDragging) {
                            e.preventDefault();
                            e.stopPropagation();
                        }
                    }, true);
                });
            });

